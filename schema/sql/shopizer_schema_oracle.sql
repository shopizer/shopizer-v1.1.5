FOR EACH ROW
DECLARE 
v_newVal NUMBER(12) := 0;
v_incval NUMBER(12) := 0;
BEGIN
  IF INSERTING AND :new.CENTRAL_FUNCTION_ID IS NULL THEN
    SELECT  CENTRAL_FUNCTION_CENTRAL_FUNCT.NEXTVAL INTO v_newVal FROM DUAL;
    -- If this is the first time this table have been inserted into (sequence == 1)
    IF v_newVal = 1 THEN 
      --get the max indentity value from the table
      SELECT NVL(max(CENTRAL_FUNCTION_ID),0) INTO v_newVal FROM CENTRAL_FUNCTION;
      v_newVal := v_newVal + 1;
      --set the sequence to that value
      LOOP
           EXIT WHEN v_incval>=v_newVal;
           SELECT CENTRAL_FUNCTION_CENTRAL_FUNCT.nextval INTO v_incval FROM dual;
      END LOOP;
    END IF;
    --used to emulate LAST_INSERT_ID()
    --mysql_utilities.identity := v_newVal; 
   -- assign the value from the sequence to emulate the identity column
   :new.CENTRAL_FUNCTION_ID := v_newVal;
